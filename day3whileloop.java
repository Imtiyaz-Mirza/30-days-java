public class day3whileloop {
    public static void main(String args[]) {
        int i = 1;
        while (i <= 5) {
            System.out.println(i);
            i++;
        }
        int j = 5;
        while (j >= 1) {
            System.out.println(j);
            j--;

        }
        int k = 1;
        while (k <= 5) {
            System.out.println("Task" + k);
            k++;
        }
    }
}
